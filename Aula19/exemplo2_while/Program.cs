using System;

namespace Exemplo_2___While
{
    class Program
    {
        static void Main(string[] args)
        {
            //Variavel
            int i = 0; //Valor inicial

            while (i < 5) //Valor final
            {
                Console.WriteLine(i); //Resultado
                i++; //Passo
            }
        }
    }
}
